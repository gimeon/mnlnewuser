// 주어진 기관명에 적용할 직급 사전을 반환.
window.resolveRankCategory = function (organization) {
  if (!organization) return '일반';
  for (const orgKey of Object.keys(window.ORG_CATEGORY)) {
    if (organization.includes(orgKey)) return window.ORG_CATEGORY[orgKey];
  }
  return '일반';
};
